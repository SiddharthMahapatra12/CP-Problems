    #include <iostream>
    #include <string>
     
    using namespace std;
     
    int main()
    {
        int i=0;
        string S;
        getline (cin, S);
        
        while(i<=S.length())
        {
            if(S[i]>=65 && S[i]<=90)
                S[i]=S[i]+32;
            else if(S[i]>=97 && S[i]<=122)
                S[i]=S[i]-32;
            i++;
        }
        
        cout<<S;
        return 0;
    }
